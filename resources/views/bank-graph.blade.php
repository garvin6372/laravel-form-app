<script>
    const rawData = @json($transactions);

    console.log(rawData)
    const labels = rawData.map(item => item.date);
    const dates = rawData.map(item => item.date);
    const moneyIn = rawData.map(item => item.moneyIn ? parseFloat(item.moneyIn) : 0);
    const moneyOut = rawData.map(item => item.moneyOut ? parseFloat(item.moneyOut) : 0);
    const balance = rawData.map(item => item.balance ? parseFloat(item.balance) : 0);

    const ctx = document.getElementById('bankChart').getContext('2d');
    new Chart(ctx, {
        type: 'line',
        data: {
            labels: labels,
            datasets: [{
                    label: 'Money In',
                    data: moneyIn,
                    borderColor: 'green',
                    fill: false
                },
                {
                    label: 'Money Out',
                    data: moneyOut,
                    borderColor: 'red',
                    fill: false
                },
                {
                    label: 'Balance',
                    data: balance,
                    borderColor: 'blue',
                    fill: false
                }
            ]
        },
        options: {
            responsive: true,
            plugins: {
                title: {
                    display: true,
                    text: 'Bank Statement Overview'
                }
            },
            scales: {
                y: {
                    beginAtZero: true
                }
            }
        }
    });

    const ctxBalance = document.getElementById('balanceChart').getContext('2d');
    new Chart(ctxBalance, {
        type: 'line',
        data: {
            labels: dates,
            datasets: [{
                label: 'Balance',
                data: balance,
                borderColor: 'blue',
                fill: false,
                tension: 0.4
            }]
        }
    });

    // Money In/Out Bar Chart
    const ctxMoney = document.getElementById('moneyChart').getContext('2d');
    new Chart(ctxMoney, {
        type: 'bar',
        data: {
            labels: dates,
            datasets: [{
                    label: 'Money In',
                    data: moneyIn,
                    backgroundColor: 'green'
                },
                {
                    label: 'Money Out',
                    data: moneyOut,
                    backgroundColor: 'red'
                }
            ]
        }
    });

    const totalMoneyIn = moneyIn.reduce((acc, val) => acc + val, 0);
    const totalMoneyOut = moneyOut.reduce((acc, val) => acc + val, 0);

    // Pie Chart for Money In vs Money Out
    const ctxPie = document.getElementById('moneyPieChart').getContext('2d');
    new Chart(ctxPie, {
        type: 'pie',
        data: {
            labels: ['Money In', 'Money Out'],
            datasets: [{
                data: [totalMoneyIn, totalMoneyOut],
                backgroundColor: ['green', 'red']
            }]
        },
        options: {
            responsive: true,
            plugins: {
                legend: {
                    position: 'bottom',
                }
            }
        }
    });

        // Calculate Total Credit & Debit
    let totalCredit = 0;
    let totalDebit = 0;

    rawData.forEach(item => {
        if (item.type === 'Credit') {
            totalCredit += parseFloat(item.moneyIn || 0);
        } else if (item.type === 'Debit') {
            totalDebit += parseFloat(item.moneyOut || 0);
        }
    });

    // Monthly Summary (group by month name)
    const monthlySummary = {};
    rawData.forEach(item => {
        const date = new Date(item.date);
        const month = date.toLocaleString('default', { month: 'short', year: 'numeric' });

        if (!monthlySummary[month]) {
            monthlySummary[month] = { credit: 0, debit: 0 };
        }

        if (item.type === 'Credit') {
            monthlySummary[month].credit += parseFloat(item.moneyIn || 0);
        } else if (item.type === 'Debit') {
            monthlySummary[month].debit += parseFloat(item.moneyOut || 0);
        }
    });

    const monthlyLabels = Object.keys(monthlySummary);
    const monthlyCredit = monthlyLabels.map(month => monthlySummary[month].credit);
    const monthlyDebit = monthlyLabels.map(month => monthlySummary[month].debit);

    // Pie Chart
    new Chart(document.getElementById('creditDebitPie'), {
        type: 'pie',
        data: {
            labels: ['Credit', 'Debit'],
            datasets: [{
                data: [totalCredit, totalDebit],
                backgroundColor: ['#4caf50', '#f44336'],
            }]
        },
        options: {
            responsive: true,
            plugins: { legend: { position: 'bottom' } }
        }
    });

    // Doughnut Chart
    new Chart(document.getElementById('creditDebitDoughnut'), {
        type: 'doughnut',
        data: {
            labels: ['Credit', 'Debit'],
            datasets: [{
                data: [totalCredit, totalDebit],
                backgroundColor: ['#2196f3', '#ff9800'],
            }]
        },
        options: {
            responsive: true,
            plugins: { legend: { position: 'bottom' } }
        }
    });

    // Monthly Summary Bar Chart
    new Chart(document.getElementById('monthlySummaryChart'), {
        type: 'bar',
        data: {
            labels: monthlyLabels,
            datasets: [
                {
                    label: 'Credit',
                    backgroundColor: '#4caf50',
                    data: monthlyCredit,
                },
                {
                    label: 'Debit',
                    backgroundColor: '#f44336',
                    data: monthlyDebit,
                }
            ]
        },
        options: {
            responsive: true,
            plugins: { legend: { position: 'bottom' } },
            scales: {
                y: { beginAtZero: true }
            }
        }
    });
